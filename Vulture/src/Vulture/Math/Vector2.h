#pragma once

#include <glm/glm.hpp>

namespace ve {

	// Vector 2 integer types
	using Vector2i = glm::ivec2;
	using Vector2u = glm::uvec2;

	// 8 bits vector 2 integer types
	using Vector2i8 = glm::i8vec2;
	using Vector2u8 = glm::u8vec2;

	// 16 bits vector 2 integer types
	using Vector2i16 = glm::i16vec2;
	using Vector2u16 = glm::u16vec2;

	// 32 bits vector 2 integer types
	using Vector2i32 = glm::i32vec2;
	using Vector2u32 = glm::u32vec2;

	// 64 bits vector 2 integer types
	using Vector2i64 = glm::i64vec2;
	using Vector2u64 = glm::u64vec2;

	// Vector 2 float type
	using Vector2f = glm::fvec2;

	// Vector 2 double type
	using Vector2d = glm::dvec2;
}
