#pragma once

#include <glm/glm.hpp>

namespace ve {

	// Vector 3 integer types
	using Vector3i = glm::ivec3;
	using Vector3u = glm::uvec3;

	// 8 bits vector 3 integer types
	using Vector3i8 = glm::i8vec3;
	using Vector3u8 = glm::u8vec3;

	// 16 bits vector 3 integer types
	using Vector3i16 = glm::i16vec3;
	using Vector3u16 = glm::u16vec3;

	// 32 bits vector 3 integer types
	using Vector3i32 = glm::i32vec3;
	using Vector3u32 = glm::u32vec3;

	// 64 bits vector 3 integer types
	using Vector3i64 = glm::i64vec3;
	using Vector3u64 = glm::u64vec3;

	// Vector 3 float type
	using Vector3f = glm::fvec3;

	// Vector 3 double type
	using Vector3d = glm::dvec3;
}
