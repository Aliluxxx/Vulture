#pragma once

#include <glm/glm.hpp>

namespace ve {

	// Vector 4 integer types
	using Vector4i = glm::ivec4;
	using Vector4u = glm::uvec4;

	// 8 bits vector 4 integer types
	using Vector4i8 = glm::i8vec4;
	using Vector4u8 = glm::u8vec4;

	// 16 bits vector 4 integer types
	using Vector4i16 = glm::i16vec4;
	using Vector4u16 = glm::u16vec4;

	// 32 bits vector 4 integer types
	using Vector4i32 = glm::i32vec4;
	using Vector4u32 = glm::u32vec4;

	// 64 bits vector 4 integer types
	using Vector4i64 = glm::i64vec4;
	using Vector4u64 = glm::u64vec4;

	// Vector 4 float type
	using Vector4f = glm::fvec4;

	// Vector 4 double type
	using Vector4d = glm::dvec4;
}
